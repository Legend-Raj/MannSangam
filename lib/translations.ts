// Helper function to get translation
export function getTranslation(language: string, key: string, ...args: any[]): string {
  const translations_obj = translations[language] || translations.english
  let text = translations_obj[key] || key

  // Replace placeholders with arguments
  if (args.length > 0) {
    args.forEach((arg, index) => {
      text = text.replace(`{${index}}`, arg)
    })
  }

  return text
}

